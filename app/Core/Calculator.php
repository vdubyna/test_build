<?php

class Core_Calculator
{
    /**
     * @param int $a
     * @param int $b
     * @return float
     */
    public function sum($a, $b)
    {
        return $a - $b;
    }

    /**
     * @param int $a
     * @param int $b
     * @return float
     */
    public function division($a, $b)
    {
        return $a / $b;
    }
}
